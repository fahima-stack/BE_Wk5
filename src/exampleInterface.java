
public interface exampleInterface { //class can implement multiple interfaces unlike inheritance where you can only inherit one class 

/*
 * Difference between abstract class and interface:
 * -Interface doesn't define any method implementations
 * -Abstract class has some abstract methods and some methods that have bodies with implementations defined.
 * -Interface you only define the method signature. Use when you know what the class implementing the interface will do but don't know how you want it to happen. You leave the implementation 100% up to that class.  
 * -Interface is like a contract; when a class implements an interface it has to create a concrete implementation of each of the methods defined in the interface. 
 */
}
